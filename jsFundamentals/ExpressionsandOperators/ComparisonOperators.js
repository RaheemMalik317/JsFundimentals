//Comparison Operators

//Equal to
//JS assumed that we are trying to check if tthe string '3' is equal to the number 3
'3' == 3;
console.log('3'  == 3); //true

//Strict Equal to
//Compares the datatype and value of the two values to check if they are equal

console.log('3'=== 3); //false


//Not equal to
console.log('3' != 3); //false


//Strict not equal to
console.log('3' !== 3); //true

//Greater Than Operator
console.log(3 > 2); //true
console.log(3 < 2); //false

//Less Than Operator
console.log(2 < 3); //true
console.log(2 > 3); //false

//Greater than or equal to
console.log(3 >= 2); //true

//Less than or equal to
console.log(2 <= 3); //true

//'And' operator

2 && 3;

//'Or' operator

2 || 3; 